#include "3-calc.h"

/**
  * op_add - add 2 ints
  * @a : input int
  * @b : input int
  * Return: will return sum of two ints
  */

int op_add(int a, int b)
{
	return ((a) + (b));
}

/**
  * op_sub - sub 2 ints
  * @a : input int
  * @b : input int
  * Return: will return sub of two ints
  */

int op_sub(int a, int b)
{
	return ((a) - (b));
}

/**
  * op_mul - multiply 2 ints
  * @a : input int
  * @b : input int
  * Return: will return mul of two ints
  */

int op_mul(int a, int b)
{
	return ((a) * (b));
}

/**
  * op_div - div 2 ints
  * @a : input int
  * @b : input int
  * Return: will divided of two ints
  */

int op_div(int a, int b)
{
	return ((a) / (b));
}

/**
  * op_mod - reminder of 2 ints
  * @a : input int
  * @b : input int
  * Return: will return reminder of two ints
  */

int op_mod(int a, int b)
{
	return ((a) % (b));
}
